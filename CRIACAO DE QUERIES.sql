-- QUANTIDADE DE CASAS POR REGIAO -- 
SELECT `REGIAO`,COUNT(*) AS TOTAL 
FROM `CASAS` GROUP BY `REGIAO` 
ORDER BY TOTAL DESC;

-- PERSONAGENS QUE MAIS APARECERAM -- 
SELECT * FROM `PERSONAGENS` WHERE `EPISODIOS_QUE_APARECEU` > 50
ORDER BY `EPISODIOS_QUE_APARECEU` DESC;


-- QUANTOS EPISODIOS POSSUI -- 
SELECT SUM(EPISODIO) 
AS TOTAL 
FROM `EPISODIOS`;

-- QUEM DIRIGIU MAIS EPISODIOS -- 
SELECT `DIRETOR`, COUNT(*) AS TOTAL  
FROM `EPISODIOS` GROUP BY `DIRETOR` 
ORDER BY TOTAL DESC;

-- TEMPORADAS MAIS LONGAS -- 
 SELECT `TEMPORADA` , sum(`DURACAO`)
 AS `DURACAO DA TEMPORADA` 
 FROM `EPISODIOS` 
 GROUP BY `TEMPORADA` 
 ORDER BY `DURACAO DA TEMPORADA` DESC LIMIT 3;












